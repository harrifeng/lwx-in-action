d = {}
d[1] = [[1, 2], [3, 4], ['US', 'UK', 'CA']]
d[2] = [[11, 22], [33, 44], ['US', 'UK', 'CA', 'CN']]


print('d: is ', d)
new_d = {}
for k, v in d.items():
    for country in v[2]:
        new_key = '{}-{}'.format(k, country)
        new_d[new_key] = v[:2]
print('new_d: is ', new_d)


